package com.careerit.corejava.day1;

public class SumOfTwoNumbers {
  public static void main(String[] args) {
    int num1 = 30;
    int num2 = 20;
    System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
    System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
    System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
    System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
    System.out.println(num1 + " % " + num2 + " = " + (num1 % num2));

  }
}
